# 检查字符串

# 检查是否以指定的字符串开头 startswith()
str1 = 'hello world'
print(str1.startswith('HE'))
# 检查是否以指定的字符串结尾 endswith()
print(str1.endswith('HE'))
# 获取字符串首字母大写
print(str1.capitalize())
# 获取字符串变大写
print(str1.upper())
# 所有大写转小写
print(str1.lower())
# 把字符串的每个首字母大学
print(str1.title())
print('------------------')
